  final String id;
  final String title;
  final String teacher;
  final String room;
  final String startTime;
  final String endTime;
  final String day;

  const Lesson({
    required this.id,
    required this.title,
    required this.teacher,
    required this.room,
    required this.startTime,
    required this.endTime,
    required this.day,
  });
}
